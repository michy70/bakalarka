    public void zapisanieSkore() throws IOException{
        File subor = new File("pomocka.txt");
        PrintWriter zapisovac = new PrintWriter(subor);
        zapisovac.flush();
        zapisovac.println(this.jSpinnerScoreA.getValue());
        zapisovac.println(this.jSpinnerScoreB.getValue());
        zapisovac.println(this.jLabelTretina.getText());
        zapisovac.close();
    }
    
    public void zapis(String nazov) throws IOException{
        File subor = new File(nazov);
        PrintWriter zapisovac = new PrintWriter(subor);
        zapisovac.println(this.jLabelDomaci.getText());
        zapisovac.println(this.jLabelHostia.getText());
        zapisovac.println(this.jSpinnerScoreA.getValue());
        zapisovac.println(this.jSpinnerScoreB.getValue());
        zapisovac.println(this.jLabelTretina.getText());

        
        zapisUdajov(zapisovac);

        zapisovac.close();
    }
    
    public void zapisUdajov(PrintWriter zapisovac) throws IOException{
        BufferedReader citacA = new BufferedReader(new FileReader(supiskaA));
        String pomoc = "";
        
        zapisovac.println("Domaci");
        while((pomoc = citacA.readLine()) != null){
            zapisovac.println(pomoc);
        }
        citacA.close();
        
        citacA = new BufferedReader(new FileReader(supiskaB));
        zapisovac.println("Hostia");
        while((pomoc = citacA.readLine()) != null){
            zapisovac.println(pomoc);
        }
        citacA.close();

        citacA = new BufferedReader(new FileReader(priebeh));
        zapisovac.println("Priebeh");
        while((pomoc = citacA.readLine()) != null){
            zapisovac.println(pomoc);
        }
        citacA.close();
    }
    
