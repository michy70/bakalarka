    public void nacitat(String nazov) throws IOException{
        BufferedReader citac = new BufferedReader(new FileReader(nazov));
        nacitatUdaje(citac);
        citac.close();
    }
    
    public void nacitatUdaje(BufferedReader citac) throws IOException{
        this.jLabelDomaci.setText(citac.readLine());
        this.jLabelHostia.setText(citac.readLine());
        String pomoc = "";
        File subor = new File("pomocka.txt");
        Scanner citac1 = new Scanner(subor);
        if(citac1.hasNext()){
            this.jSpinnerScoreA.setValue(Integer.parseInt(citac1.next()));
            this.jSpinnerScoreB.setValue(Integer.parseInt(citac1.next()));
            this.jLabelTretina.setText(citac1.next());
        } else if((pomoc = citac.readLine()) != null){
            this.jSpinnerScoreA.setValue(Integer.parseInt(pomoc));
            pomoc = citac.readLine();
            this.jSpinnerScoreB.setValue(Integer.parseInt(pomoc));
            pomoc = citac.readLine();
            this.jLabelTretina.setText(pomoc);
            if((pomoc = citac.readLine()) != null){
                nacitatSupiskyPriebeh(citac);  
            }
        }
        citac1.close();
    }
    
    public void nacitatSupiskyPriebeh(BufferedReader citac) throws FileNotFoundException, IOException{
        String pomoc = "";
        
        File subor = new File(supiskaA);
        PrintWriter zapisovac = new PrintWriter(subor);
        while(!(pomoc = citac.readLine()).equals("Hostia")){
            zapisovac.println(pomoc);
        }
        zapisovac.close();
        
        subor = new File(supiskaB);
        zapisovac = new PrintWriter(subor);
        while(!(pomoc = citac.readLine()).equals("Priebeh")){
            zapisovac.println(pomoc);
        }
        zapisovac.close();
        
        subor = new File(priebeh);
        zapisovac = new PrintWriter(subor);
        while((pomoc = citac.readLine()) != null){
            zapisovac.println(pomoc);
        }
        zapisovac.close();
    }

