    public void ulozenie() throws FileNotFoundException{
        File subor = new File(nazov);
        PrintWriter zapisovac = new PrintWriter(subor);
        zapisovac.flush();
        zapisovac.println("Tabulka Golov");
        ulozenieGolovAleboTrestov(zapisovac, tabulkaGole);
        zapisovac.println("Tabulka Trestov");
        ulozenieGolovAleboTrestov(zapisovac, tabulkaTrestov);
        zapisovac.close();
    }
    
    public void ulozenieGolovAleboTrestov(PrintWriter zapisovac, JTable tabulka){
        int pocetRiadkov = tabulka.getModel().getRowCount();
        for(int i = 0;i<pocetRiadkov*4;i++){
            if(tabulka.getModel().getValueAt(i / 4, i % 4) != null){
                zapisovac.println(tabulka.getModel().getValueAt(i / 4, i % 4));
            }
        }
        zapisovac.println(";");
    }
    
