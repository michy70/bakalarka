    public void nacitanie() throws IOException{
        BufferedReader citac = new BufferedReader(new FileReader(nazov));
        String pomoc = "";
        while((pomoc = citac.readLine()) != null){
            if(pomoc.equals("Tabulka Golov")){
                nacitanieGolovAleboTrestov(citac, tabulkaGole);
            } else if(pomoc.equals("Tabulka Trestov")){
                nacitanieGolovAleboTrestov(citac, tabulkaTrestov);
            }
        }
        citac.close();
    }
    
    public void nacitanieGolovAleboTrestov(BufferedReader citac, JTable tabulka) throws IOException{
        int HH = 1;
        for(int i = 0;i<HH;i++){
            String data = citac.readLine();
            if(!data.equals(";")){
                if(i%4 == 0){
                    DefaultTableModel model = (DefaultTableModel) tabulka.getModel();
                    model.addRow(new Object[]{});
                }
                tabulka.getModel().setValueAt(data, i/4, i%4);
                HH++;
            }  
        }
    }

