    public void nacitanie() throws IOException{
        File subor = new File(nazov);
        BufferedReader citac = new BufferedReader(new FileReader(nazov));
        String pomoc = "";
        while((pomoc = citac.readLine()) != null){
            if(pomoc.equals("Manazment")){
                nacitanieManazment(citac);
            } else if(pomoc.equals("Zostava")){
                nacitanieZostava(citac);
            } else if(pomoc.equals("Supiska")){
                nacitanieSupiska(citac);
            }
        }
        citac.close();
    }
