    public void ulozenieSupiska(PrintWriter zapisovac){
        zapisovac.println("Supiska");
        for(int i = 0;i<100;i++){
            if(supiska.getModel().getValueAt(i / 4, i % 4) != null){
                zapisovac.println(supiska.getModel().getValueAt(i / 4, i % 4));
            } else {
                i = 100;
            }
        }
        zapisovac.println(";");
    }
    
