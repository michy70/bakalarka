    public void ulozenie() throws FileNotFoundException{
        File subor = new File(nazov);
        PrintWriter zapisovac = new PrintWriter(subor);
        zapisovac.flush();
        ulozenieSupiska(zapisovac);
        ulozenieManazment(zapisovac);
        ulozenieZostava(zapisovac);
        zapisovac.close();
    }
